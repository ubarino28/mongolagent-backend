function buildWebsiteKnowledge() {
  const pages = WEBSITE_PAGES.map((p) => {
    const lines = [`• ${p.label} — ${p.what} Хийх нь: ${p.how}`];
    if (p.tips) lines.push(`  Санамж: ${p.tips}`);
    return lines.join("\n");
  }).join("\n\n");
  const sections = SECTIONS.map((s) => `• ${s.t}: ${s.d}`).join("\n");
  const templates = TEMPLATES_INFO.map((t) => `• ${t.name}: ${t.d}`).join("\n");
  return `━━━ ВЭБСАЙТЫН УДИРДЛАГЫН ХУУДСУУД (sidebar цэс бүр) ━━━\n\n${pages}\n\n━━━ SECTION (БЛОК) ГАРЫН АВЛАГА — тус бүр юу хийдэг, юунд хэрэгтэй ━━━\n\n${sections}\n\n━━━ ДЭЛГҮҮР ЗАГВАР ━━━\n\n${templates}`;
}

// Route → хуудасны нэр (тодорхой /settings/* эхэнд байх тул тэдгээр эхэлж таарна)
function pageLabelForRoute(route) {
  if (!route) return null;
  const clean = String(route).split("?")[0];
  const p = PAGES.find((x) => x.route === clean)
    || PAGES.find((x) => x.route.startsWith("/") && clean.startsWith(x.route + "/"));
  return p ? p.label : null;
}

// Бүх заавраас system prompt-ийн мэдлэгийн хэсэг угсарна
function buildHelpKnowledge() {
  return PAGES.map((p) => {
    const lines = [`• ${p.label} — ${p.what} Хийх нь: ${p.how}`];
    if (p.tips) lines.push(`  Санамж: ${p.tips}`);
    if (Array.isArray(p.faq) && p.faq.length) {
      lines.push(`  Түгээмэл асуулт: ${p.faq.join(" | ")}`);
    }
    return lines.join("\n");
  }).join("\n\n");
}

