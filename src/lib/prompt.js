      if (prod.targetUser) p += `\n  Хэнд: ${prod.targetUser}`;
      if (prod.features) p += `\n  Онцлог: ${prod.features}`;
      if (prod.objection && prod.objectionResponse) {
        p += `\n  "${prod.objection}" → "${prod.objectionResponse}"`;
      }
