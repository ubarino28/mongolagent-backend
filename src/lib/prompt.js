// ─── SYSTEM PROMPT BUILDER ────────────────────────────────────────────────────

async function buildSystemPrompt(isNew, orgId = null) {
  let bodyPrompt = null;
  let businessType = null;

  try {
    const prisma = getPrisma();
    const settings = await prisma.turuuSettings.findMany({
      where: {
        orgId,
        key: { in: ["system_prompt", "ai_profile", "ai_company", "ai_name", "ai_contact", "ai_extra_rules", "business_type"] },
      },
    });

    const s = {};
    settings.forEach((r) => { s[r.key] = r.value; });
    businessType = s.business_type || null;

    // Priority: ai_profile (narrative) → system_prompt (custom) → ai_company (old)
    if (s.ai_profile) {
      try {
        const profile = JSON.parse(s.ai_profile);
        bodyPrompt = buildNarrativePrompt(profile);
      } catch { /* fallback */ }
    }

    if (!bodyPrompt && s.system_prompt) {
      bodyPrompt = s.system_prompt;
    }

    if (!bodyPrompt && s.ai_company) {
      bodyPrompt = buildCoreTemplate({
        company:    s.ai_company,
        aiName:     s.ai_name || "AI туслах",
        contact:    s.ai_contact || "",
        extraRules: s.ai_extra_rules || "",
      });
    }
  } catch {
    // DB unavailable — defaults
  }

  const newConvLine = isNew
    ? `ШИНЭ ЯРИА: Мэндлэлийг хариулттай НЭГЭН ЗЭРЭГ нэг мессежид бич. Зөвхөн "сайн уу"/"hi" ирвэл "Сайн байна уу! 😊 Танд юугаар туслах вэ?" гэ.`
    : `ҮРГЭЛЖИЛЖ БУЙ ЯРИА: Яриагаа үргэлжлүүл. "Сайн байна уу?" давтахгүй.`;

  const imageBlock = `━━━━━━━━━━━━━━━━━━━━━━━━━
ЗУРАГ ИРЭХЭД — ЗААВАЛ ДАГАХ
━━━━━━━━━━━━━━━━━━━━━━━━━
Хэрэглэгч зураг илгээвэл:
1. Зургаас бараа/бүтээгдэхүүний нэрийг тодорхойл
2. search_knowledge(тухайн барааны нэр) ЗААВАЛ дуудаж KB-д байгаа эсэхийг шалга
3. KB-д байвал → тухайн барааны мэдээллээр хариул (үнэ, хувилбар гэх мэт)
4. KB-д байхгүй бол → "Уучлаарай, энэ бараа манай санд бүртгэлгүй байна" гэ
✗ Зургаас таасан нэр/брэнд/загварыг KB шалгалгүй хэзээ ч хэлэхгүй`;

  const variantImageBlock = `━━━━━━━━━━━━━━━━━━━━━━━━━
ТОДОРХОЙ ӨНГӨ/ХУВИЛБАРЫН ЗУРАГ АСУУХАД
━━━━━━━━━━━━━━━━━━━━━━━━━
Хэрэглэгч "энэ өнгийн зураг байна уу?" гэх мэт асуувал:
1. search_knowledge(бараа нэр + өнгө) дуудаж "Зурагтай өнгөнүүд" жагсаалтыг шалга
2. Тухайн өнгө жагсаалтад БАЙВАЛ → зураг автоматаар хавсардаг тул "Тийм, энд байна 😊" гэх мэтээр баталгаажуулж хариул
3. Тухайн өнгө жагсаалтад БАЙХГҮЙ бол → "Уучлаарай, [өнгө]-н зураг одоогоор бэлэн биш байна" гэ
✗ "Манай санд бүртгэлгүй байна" гэж хэзээ ч хэлэхгүй — бараа өөрөө бүртгэлтэй, зөвхөн тухайн өнгийн зураг л дутуу байж болзошгүй`;

  const visualMatchBlock = `━━━━━━━━━━━━━━━━━━━━━━━━━
ХЭРЭГЛЭГЧИЙН ЗУРГИЙГ САНГИЙН ЗУРАГТАЙ ХАРЬЦУУЛАХ
━━━━━━━━━━━━━━━━━━━━━━━━━
Хэрэглэгчийн илгээсэн зургийн дараа сангийн хувилбаруудын зургууд (лэйблтэй) ирвэл, эдгээрийг хэрэглэгчийн зурагтай VISUAL-аар харьцуул:
1. Хэрэглэгчийн зураг сангийн зургуудын аль нэгтэй ИЖИЛ/МАШ ТӨСТЭЙ бол → тухайн лэйблийн (размер/өнгө) мэдээллээр "Тийм, байгаа" гэх мэт баталгаажуулж хариул — нэр/өнгөний нэршил яг таарахгүй ч зураг таарвал итгэ
2. Ямар нэг сангийн зурагтай таарахгүй бол → "Уучлаарай, энэ загвар/өнгө манай санд бүртгэлгүй байна" гэ`;

  const body = bodyPrompt || getDefaultBody();

  // Appointment block — зөвхөн org-д active staff байвал нэмнэ
  let appointmentBlock = "";
  if (orgId) {
    try {
      const prismaInst = getPrisma();
      const staffCount = await prismaInst.turuuStaff.count({ where: { orgId, isActive: true } });
      if (staffCount > 0) {
        const { getLabels } = require("./businessType");
        const lbl = getLabels(businessType);
        appointmentBlock = `━━━━━━━━━━━━━━━━━━━━━━━━━
${lbl.appointment.toUpperCase()}
━━━━━━━━━━━━━━━━━━━━━━━━━
Хэрэглэгч цаг захиалах, цаг авах, appointment хүсвэл:
1. check_staff дуудаж ${lbl.staffPlural} жагсаалт, тэдний үйлчилгээ, ажлын цагийг ол
2. Олон ${lbl.staff} байвал → "Аль ${lbl.staffWith} цаг авах вэ?" гэж нэг асуулт асуу
3. ${lbl.staffCap} тодорхойлогдсон бол → ямар үйлчилгээ авах вэ гэдгийг ол (check_staff-ийн жагсаалтаас)
4. Ямар өдөр авах вэ гэдгийг ол → огноог YYYY-MM-DD болгоно
5. check_availability(staffId, date, serviceName) дуудаж боломжит цагуудыг ол
   → Цагуудыг тоочин харуул → хэрэглэгч нэгийг сонгосны дараа цааш яв
6. Нэр, утасны дугаар ав
7. БАТАЛГААЖУУЛ: "Цаг захиалгаа баталгаажуулна уу?\\n📅 [date] [timeSlot]\\n💆 ${lbl.staffCap}: [staffName]\\n✂️ Үйлчилгээ: [serviceName]\\n👤 [customerName]\\n📞 [customerPhone]\\nЗөв үү?"
8. Баталгаажуулсны дараа save_appointment дуудна
9. save_appointment амжилттай бол:
   - qpay талбар байвал (урьдчилгаатай) → "Цагаа захиаллаа! Урьдчилгаа ₮[amount] төлнө үү 👇" гэж QPay холбоосуудыг жагсаан харуул. Төлбөр хийгдмэгц автоматаар баталгаажна гэж мэдэгд
   - qpay талбар байхгүй бол (урьдчилгаагүй) → "Цагаа амжилттай захиаллаа! 🎉"
10. save_appointment алдаа буцаавал (success: false) → хэрэглэгчид "Уучлаарай, тэр цаг захиалагдсан байна" гэж хэлээд check_availability дахин дуудаж шинэ чөлөөтэй цагуудыг санал болго
✗ check_availability дуудалгүй цаг санал болгохгүй
✗ save_appointment дуудахын өмнө нэр болон утас ЗААВАЛ авсан байна`;
      }
    } catch { /* non-blocking */ }
  }

  const parts = [body, imageBlock, variantImageBlock, visualMatchBlock];
  if (appointmentBlock) parts.push(appointmentBlock);
  parts.push(newConvLine);
  return parts.join("\n\n");
}

